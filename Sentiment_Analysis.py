        "classifier = nltk.NaiveBayesClassifier.train(featuresets)\n",
        "test_featuresets = [(document_features(d), c) for (d, c) in test_set]\n",
        "accuracy = nltk.classify.accuracy(classifier, test_featuresets)\n",
